CREATE TABLE nombre_completo_empleados (
    id_nombre_empleados INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50),
    apellido_P VARCHAR(50),
    apellido_M VARCHAR(50)
);
CREATE TABLE seccion (
    id_seccion INT AUTO_INCREMENT PRIMARY KEY,
    numero_seccion INT,
    tipo_casilla VARCHAR(50)
);
CREATE TABLE empleados (
    id_empleados INT AUTO_INCREMENT PRIMARY KEY,
    Zore VARCHAR(50),
    Are VARCHAR(50),
    id_nombre_empleados INT,
    id_seccion INT,
    telefono VARCHAR(20),
    FOREIGN KEY (id_nombre_empleados) REFERENCES nombre_completo_empleados(id_nombre_empleados),
    FOREIGN KEY (id_seccion) REFERENCES seccion(id_seccion)
);
CREATE TABLE material_electoral (
    id_material_electoral INT AUTO_INCREMENT PRIMARY KEY,
    entidad VARCHAR(50),
    distrito VARCHAR(50),
    CIME VARCHAR(50),
    material_electoral VARCHAR(50),
    salida INT,
    total_disponible INT
);
CREATE TABLE folio (
    id_folio INT AUTO_INCREMENT PRIMARY KEY,
    id_material_electoral INT,
    codigo_articulo VARCHAR(50),
    id_empleados INT,
    numeral_material_electoral VARCHAR(50),
    FOREIGN KEY (id_material_electoral) REFERENCES material_electoral(id_material_electoral),
    FOREIGN KEY (id_empleados) REFERENCES empleados(id_empleados)
);
CREATE TABLE Usuarios (
    Id INT AUTO_INCREMENT PRIMARY KEY,           -- Identificador único del usuario
    NombreUsuario VARCHAR(50) NOT NULL UNIQUE,   -- Nombre de usuario (debe ser único)
    Contrasena VARCHAR(255) NOT NULL,            -- Contraseña (puedes hashearla para mayor seguridad)
    NombreCompleto VARCHAR(100) NOT NULL,        -- Nombre completo del usuario
    Email VARCHAR(100),                          -- Correo electrónico (opcional)
    NivelAcceso ENUM('Admin', 'Usuario') NOT NULL DEFAULT 'Usuario', -- Rol de usuario
    FechaCreacion TIMESTAMP DEFAULT CURRENT_TIMESTAMP, -- Fecha de creación del usuario
    Activo BOOLEAN DEFAULT TRUE                  -- Indica si el usuario está activo
);

